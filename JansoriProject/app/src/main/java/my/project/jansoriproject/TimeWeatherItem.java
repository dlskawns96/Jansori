package my.project.jansoriproject;

public class TimeWeatherItem {

    private String time;
    private String temp;
    private String rainchance;
    private String skyCode;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getRainchance() {
        return rainchance;
    }

    public void setRainchance(String rainchance) {
        this.rainchance = rainchance;
    }

    public String getSkyCode() {
        return skyCode;
    }

    public void setSkyCode(String skyCode) {
        this.skyCode = skyCode;
    }
}
