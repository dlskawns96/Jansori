package my.project.jansoriproject;

public class ArrivalItem {

    String pathType;
    String laneName;
    String arrmsg1;
    String arrmsg2;
    Integer traTime1;
    Integer traTime2;

    public String getPathType() {
        return pathType;
    }

    public void setPathType(String pathType) {
        this.pathType = pathType;
    }

    public String getLaneName() {
        return laneName;
    }

    public void setLaneName(String laneName) {
        this.laneName = laneName;
    }

    public String getArrmsg1() {
        return arrmsg1;
    }

    public void setArrmsg1(String arrmsg1) {
        this.arrmsg1 = arrmsg1;
    }

    public String getArrmsg2() {
        return arrmsg2;
    }

    public void setArrmsg2(String arrmsg2) {
        this.arrmsg2 = arrmsg2;
    }

    public Integer getTraTime1() {
        return traTime1;
    }

    public void setTraTime1(Integer traTime1) {
        this.traTime1 = traTime1;
    }

    public Integer getTraTime2() {
        return traTime2;
    }

    public void setTraTime2(Integer traTime2) {
        this.traTime2 = traTime2;
    }
}
